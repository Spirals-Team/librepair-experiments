package net.posesor.subjects;

import lombok.Data;
import org.hibernate.validator.constraints.NotEmpty;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.CompoundIndex;
import org.springframework.data.mongodb.core.mapping.Document;

import javax.validation.constraints.NotNull;

/**
 * Defines properties for store Subject.
 */
@Document(collection = "subjects")
@CompoundIndex(name = "principal_and_subjectName", def = "{'principalName':1, 'subjectName':1}", unique = true)
@Data
public class SubjectDbModel {

    /**
     * Unique subject id, generated by storage and used in relations.
     */
    @Id
    private String subjectId;

    /**
     * Data owner.
     */
    @NotNull
    @NotEmpty
    private String principalName;

    /**
     * Name of the subject, unique in scope of principal's subjects.
     */
    @NotNull
    @NotEmpty
    private String subjectName;
}

