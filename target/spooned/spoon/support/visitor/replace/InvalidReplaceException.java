package spoon.support.visitor.replace;


public class InvalidReplaceException extends spoon.SpoonException {
    public InvalidReplaceException() {
    }

    public InvalidReplaceException(java.lang.String msg) {
        super(msg);
    }

    public InvalidReplaceException(java.lang.Throwable e) {
        super(e);
    }

    public InvalidReplaceException(java.lang.String msg, java.lang.Throwable e) {
        super(msg, e);
    }
}

