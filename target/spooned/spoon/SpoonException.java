package spoon;


public class SpoonException extends java.lang.RuntimeException {
    private static final long serialVersionUID = 1L;

    public SpoonException() {
        super();
    }

    public SpoonException(java.lang.String msg) {
        super(msg);
    }

    public SpoonException(java.lang.Throwable e) {
        super(e);
    }

    public SpoonException(java.lang.String msg, java.lang.Throwable e) {
        super(msg, e);
    }
}

