package to.etc.formbuilder.pages;

public class JsonComponentInfo {
	private String m_typeId;

	private String m_id;

	private int m_x;

	private int m_y;

	public String getTypeId() {
		return m_typeId;
	}

	public void setTypeId(String typeId) {
		m_typeId = typeId;
	}

	public String getId() {
		return m_id;
	}

	public void setId(String id) {
		m_id = id;
	}

	public int getX() {
		return m_x;
	}

	public void setX(int x) {
		m_x = x;
	}

	public int getY() {
		return m_y;
	}

	public void setY(int y) {
		m_y = y;
	}
}
